class StateNameValidator extends Validator {
    isValid() {
        this.reset();

        if (STATE_LIST.indexOf(this.value) !== -1) {
            return true;
        }

        this.addError('"' + this.value + '" is not a valid state');
        return false;
    }
}

VALIDATORS.register('StateName', StateNameValidator);
